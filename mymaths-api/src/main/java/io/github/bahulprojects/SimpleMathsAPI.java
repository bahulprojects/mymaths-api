package io.github.bahulprojects;

public class SimpleMathsAPI {
	public long add(int a, int b) {
		return a + b;
	}
	
	public long multiply(long a, long b) {
		return a * b;
	}
	
	public float divide (float a, float b) {
		return a/b;
	}
	
	public long subtract (long a, long b) {
		return a - b;
	}
}
